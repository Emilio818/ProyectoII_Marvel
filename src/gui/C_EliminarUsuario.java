    private void llenarLineas() {
        ArrayList<Usuario> usuarios = ManejoJSON.leerJSON(Usuario.RUTA, Usuario.class);
        for (int i = 0; i < usuarios.size(); i++){
            String[] info = new String[3];
            info[0] = usuarios.get(i).getNombreUsuario();
            info[1] = usuarios.get(i).getNombre();
            info[2] = usuarios.get(i).getApellidos();
            modelo.addRow(info);
        }      
    }

    private void llenarTabla() {
        modelo = new DefaultTableModel();
        modelo.addColumn("Usuario");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellidos");
        llenarLineas();
        this.tabla.setModel(modelo);
    }
